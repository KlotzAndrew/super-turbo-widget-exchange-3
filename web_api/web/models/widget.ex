defmodule WebApi.Widget do
  use WebApi.Web, :model

  schema "widgets" do
    field :token, :integer

    timestamps()
  end

  @doc """
  Builds a changeset based on the `struct` and `params`.
  """
  def changeset(struct, params \\ %{}) do
    struct
    |> cast(params, [:token])
    |> validate_required([:token])
  end
end
